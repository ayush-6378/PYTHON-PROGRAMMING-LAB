s = "PDEU College"

# Extract "PDEU" (indexes 0 to 3)
print("Extract 'PDEU':", s[0:4])

# Extract "College" (indexes 5 to 11)
print("Extract 'College':", s[5:12])

# Extract "U Col" (indexes 3 to 7)
print("Extract 'U Col':", s[3:8])

# Extract string in reverse order using slice step -1
print("Reverse order:", s[::-1])